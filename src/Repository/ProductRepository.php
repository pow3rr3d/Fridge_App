    public function getAllQuery(ProductSearch $search): Query
    {
        $qb = $this->createQueryBuilder('p');
        if($search->getName())
        {
            $qb = $qb->where("p.name LIKE :name");
            $qb->setParameter(":name" , '%'.$search->getName().'%');
        }
        return $qb->getQuery();
    }

